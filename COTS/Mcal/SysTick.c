void (*callback)(void)= NULL_PTR;
/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
void Systick_setCallback(void(*ptr)(void)){
    callback=ptr;
}
void Systick_intruptEnable()
{
    STCTRL|=(1<<1);
}
void Systick_Load(uint32 counts){
    STRELOAD= counts;
}
void Systick_start(){
    STCTRL|=(1<<0);
}
void Systick_stop(){
    STCTRL&= ~(1<<0);
}
uint32 Systick_current(){
    return STCURRENT;
}
void Systick_clock(uint8 clocksource){
     if(clocksource == 4)
        STCTRL &= ~(1<<2);
    else
        STCTRL |= (1<<2);
}


void SysTick_Handler(void){
    STCTRL &=~(1<<16);
    callback();
}



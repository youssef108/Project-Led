/******************************************************************************
* \Syntax          : void IntCrtl_Init(void)                                      
* \Description     : initialize Nvic\SCB Module by parsing the Configuration 
*                    into Nvic\SCB registers                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/
void IntCrtl_Init(void)
{

	/*TODO Configure Grouping\SubGrouping System in APINT register in SCB*/
    #if APINT_xxx==1
    APINT = 0xFA05|0x00000000;
    #elif APINT_xxy==1
    APINT = 0xFA05|0x00000500;
    #elif APINT_xyy==1
    APINT = 0xFA05|0x00000600;
    #elif APINT_yyy==1
    APINT = 0xFA05|0x00000700;
    #endif
    
	/*TODO : Enable\Disable based on user configurations in NVIC_ENx and SCB_Sys Registers */
    #if En_GPIO_PORTA==1
    En0|=(1<<(IntCtrl_InterruptType.GPIO_PORTA-16));
    #endif
    #if En_GPIO_PORTB==1
    EN0 |=(1<<((GPIO_PORTB)-16));
    #endif
    #if En_GPIO_PORTC==1
    EN0 |=(1<<((GPIO_PORTC)-16));
    #endif
    #if En_GPIO_PORTD==1
    EN0 |=(1<<((GPIO_PORTD)-16));
    #endif
    #if En_GPIO_PORTE==1
    EN0 |=(1<<((GPIO_PORTE)-16));
    #endif  
    #if En_Timer_0A==1
    EN0 |=(1<<((Timer_0A)-16));
    #endif  
    #if En_Timer_1A==1
    EN0 |=(1<<((Timer_1A)-16));
    #endif
	#if En_Timer_2A==1
    EN0 |=(1<<((Timer_2A)-16));
    #endif
    #if En_Timer_3A==1
    EN0 |=(1<<((Timer_3A)-16));
    #endif
    #if En_Timer_0B==1
    EN0 |=(1<<((Timer_0B)-16));
    #endif
     #if En_Timer_1B==1
    EN0 |=(1<<((Timer_1B)-16));
    #endif
     #if En_Timer_2B==1
    EN0 |=(1<<((Timer_2B)-16));
    #endif
     #if En_Timer_3B==1
    EN0 |=(1<<((Timer_3B)-16));
    #endif


}

